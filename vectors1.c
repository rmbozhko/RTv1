#include "rtv1.h"

double		find_cos_vectors(t_vector *v1, t_vector *v2)
{
	double cos;

	cos = (v1->x * v2->x + v1->y * v2->y + v1->z * v2->z) /
		(sqrt(v1->x * v1->x + v1->y * v1->y + v1->z * v1->z)
			* sqrt(v2->x * v2->x + v2->y * v2->y + v2->z * v2->z));
	return (cos);
}
